#include "libft.h"

char	*ft_strjoin(const char *s1, const char *s2)
{
	size_t	len_result;
	char	*result;
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (!s1 || !s2)
		return (NULL);
	len_result = ft_strlen(s1) + ft_strlen(s2);
	result = ft_calloc((len_result + 1), sizeof(char));
	if (!result)
		return (NULL);
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		result[i + j] = s2[j];
		j++;
	}
	return (result);
}