#include "libft.h"

int	ft_putnbr(int nb)
{
	char	c;
	int		i;

	i = 0;
	c = '\0';
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (nb < 0)
	{
		i += write(1, "-", 1);
		nb = -nb;
	}
	if (nb > 9)
		i += ft_putnbr(nb / 10);
	c = (nb % 10) + '0';
	i += write(1, &c, 1);
	return (i);
}